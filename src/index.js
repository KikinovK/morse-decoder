const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    return expr.match(/.{1,10}/g)
               .map(value => value === '**********'
                             ?
                                ' '
                             :
                                MORSE_TABLE[value.match(/.{1,2}/g)
                                                 .map(value => value === '10'
                                                                ?
                                                                    '.'
                                                                :
                                                                    value === '11'
                                                                    ?
                                                                        '-'
                                                                    :
                                                                        ''
                                                )
                                                .filter(value => value.lenght !== 0)
                                                .join('')
                                            ]
                ).join('')
}

module.exports = {
    decode
}
